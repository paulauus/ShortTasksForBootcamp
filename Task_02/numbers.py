num1 = int(input("Enter a whole number: "))
num2 = int(input("Enter another whole number: "))
num3 = int(input("Enter one more whole number: "))

total_sum = num1 + num2 + num3  # the sum of all the numbers
print("The sum of all the numbers is:", total_sum)

first_minus_second = num1 - num2      # first number minus second number
print("The first number minus the second number is:", first_minus_second)

third_times_first = num3*num1        # third number multiplied by first number
print("The third number multiplied by the first number is:", third_times_first)

total_div_by_third = total_sum / num3   # sum of all numbers divided by third number
print("The sum of all numbers divided bu the third number is:", total_div_by_third)